#include<iostream>
using namespace std;

int main()
{
    int n;
    cin>>n;
    int *arr=new int[n];
    for(int i=0;i<n;i++)
    {
        cin>>arr[i];
    }
    int a,flag=0;
    cout<<"enter no to be searched"<<endl;
    cin>>a;
    for(int i=0;i<n;i++)
    {
        if(arr[i]==a)
        {
            flag=1;
            cout<<"element found"<<endl;
            break;
        }
    }
    if(flag==0)
    {
        cout<<"element not found"<<endl;
    }
}

